#printing an integer value //syntax: "%[width]d"
print("%8d" %(131072))

#printing a float value //synatx: "%[width].[precision]f"
print("%7.2f" %(2.048))

#printing a number in it's octal form //syntax: "%[width].[number_of_digits]o"
print("%5.3o" %(25))

#printing a number in it's hexadecimal form //syntax: "%[width].[number_of_digits]x"
print("%5.3x" %(25))

#printing a number in it's exponential form //syntax: "%[width].[number_of_digits]e"
print("%5.4e" %(131.072))